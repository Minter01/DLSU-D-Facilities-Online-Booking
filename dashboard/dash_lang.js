// Function to update content based on selected language
function updateLanguage(language) {
    const langContent = translations[language];
    if (!langContent) return;

    document.querySelector('.quote1').textContent = langContent.quote1;
    document.querySelector('.dashboard-label').textContent = langContent.dashboardLabel;
    document.querySelector('.live-status-box h2').textContent = langContent.liveStatusTitle;
    document.querySelector('.quote2').textContent = langContent.quote2;
    document.querySelector('.available-facilities-title').textContent = langContent.availableFacilitiesTitle;
    document.querySelector('.quote').textContent = langContent.quote3;
    document.querySelector('.facility1').textContent = translations[language].facility1;
    document.querySelector('.facility2').textContent = translations[language].facility2;
    document.querySelector('.facility3').textContent = translations[language].facility3;
    document.querySelector('.facility4').textContent = translations[language].facility4;
    document.querySelector('.facility5').textContent = translations[language].facility5;
    document.querySelector('.facility6').textContent = translations[language].facility6;
    document.querySelector('.facility7').textContent = translations[language].facility7;
    document.querySelector('.facility8').textContent = translations[language].facility8;
    document.querySelector('.facility9').textContent = translations[language].facility9;

    const categoryButtons = document.querySelectorAll('.category-buttons button');
    categoryButtons[0].textContent = langContent.categoryAll;
    categoryButtons[1].textContent = langContent.categoryEvents;
    categoryButtons[2].textContent = langContent.categorySports;
    categoryButtons[3].textContent = langContent.categoryMeetings;
}

// Event listener for language dropdown
document.querySelector('.language-dropdown').addEventListener('change', (event) => {
    const selectedLanguage = event.target.value;
    updateLanguage(selectedLanguage);
});

// Set default language to English on page load
document.addEventListener('DOMContentLoaded', () => {
    updateLanguage('en');
});
